
public class Exercicio_97 {
	public static void main(String[] args) {
		//Escreva um programa que calcule e imprima a tabuada do 8 (1 a 10).
		
		int tabuada = 8;
		int contador = 1;
		
		while(contador <= 10) {
			System.out.println(tabuada + " X " + contador + " = " + (tabuada*contador));
			contador++;
		}
	}
}
